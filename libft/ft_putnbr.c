#include "libft.h"

void	ft_putnbr(int n)
{
	char	digit_char;

	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n >= 10)
		ft_putnbr(n / 10);
	digit_char = n % 10 + '0';
	write(1, &digit_char, 1);
}
